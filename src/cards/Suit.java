package cards;

/**
 * Enumerator that represents the suits of the cards.
 * @author Diogo Martins, Jose Brito, Xavier Fernandes
 *
 */
public enum Suit {
	/**
	 * Represents suit Spades
	 */
	SPADES,
	
	/**
	 * Represents suit Clubs
	 */
	CLUBS,
	
	/**
	 * Represents suit Hearts
	 */
	HEARTS,
	
	/**
	 * Represents suit Diamonds
	 */
	DIAMONDS
}
